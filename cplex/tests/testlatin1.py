"""
Tests the API with unicode strings.

No command line arguments are required.
"""
import unittest
import os
from cplextestcase import CplexTestCase

os.environ['LANG'] = 'en_US.UTF-8'


class Latin1Tests(CplexTestCase):
    nameset = ["motörhead", "Ørsted", "Gauß", "Nuñoz"]

    def testLatin1(self):
        c = self._newCplex()
        c.variables.add(names=self.nameset)
        name_out = c.variables.get_names()
        for i, n in enumerate(name_out):
            self.assertEqual(self.nameset[i], n)

        c_name_out = c.variables.get_names()
        d = self._newCplex()
        with CplexTestCase._getTempFileName(ext=".lp") as tmpfilename:
            c.write(tmpfilename)
            d.read(tmpfilename)
        d_name_out = d.variables.get_names()

        for i, n in enumerate(c_name_out):
            self.assertEqual(self.nameset[i], n)
            self.assertEqual(d_name_out[i], n)


def main():
    unittest.main()

if __name__ == '__main__':
    main()

