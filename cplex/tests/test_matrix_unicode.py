"""
Tests linear_constraints and variables with unicode strings.

No command line arguments are required.
"""
import unittest
import sys
from cplextestcase import CplexTestCase
from cplex import SparsePair


class MakeMatrixUnicodeTests(CplexTestCase):
    var_names = ["motörhead", "Ørsted", "Gauß", "Nuñoz"]

    def testLinearConstraintByRow(self):
        c0 = self._newCplex()
        varnames = self.var_names
        # First, we add the variable names in latin1.
        c0.variables.add(names=varnames)
        encnames = c0.variables.get_names()
        self._checkEncodedNames(varnames, encnames)
        # Then, we make sure we can use the encoded variables names (that they
        # map to the original latin1 names given).
        c0.linear_constraints.add(
            lin_expr=[[encnames, [1.0] * len(encnames)]])
        c0.linear_constraints.add(
            lin_expr=[SparsePair(ind=encnames,
                                 val=[1.0] * len(encnames))])

    def testLinearConstraintByColumn(self):
        c1 = self._newCplex()
        varnames = self.var_names
        # First, we add the linear constraint names in latin1.
        c1.linear_constraints.add(names=varnames)
        encnames = c1.linear_constraints.get_names()
        self._checkEncodedNames(varnames, encnames)
        # Then, we make sure we can use the encoded variables names (that they
        # map to the original latin1 names given).
        c1.variables.add(columns=[[encnames, [1.0] * len(encnames)]])
        c1.variables.add(
            columns=[SparsePair(ind=encnames,
                                val=[1.0] * len(encnames))])

    def _checkEncodedNames(self, varnames, encnames):
        for i, v in enumerate(varnames):
            self.assertTrue(isinstance(v, str))
            self.assertTrue(isinstance(encnames[i], str))
            self.assertEqual(v, encnames[i])


def main():
    unittest.main()


if __name__ == '__main__':
    main()

